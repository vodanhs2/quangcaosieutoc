<p style="text-align: justify;">T&ocirc;i sẽ d&agrave;nh một b&agrave;i viết chuy&ecirc;n s&acirc;u để hướng dẫn bạn chi tiết c&aacute;ch sử dụng v&agrave; c&aacute;c t&iacute;nh năng hiện c&oacute;.</p>

<p style="text-align: justify;">Nhưng trong giới hạn b&agrave;i viết n&agrave;y, t&ocirc;i sẽ mặc định bạn đ&atilde; từng sử dụng t&iacute;nh năng n&agrave;y.</p>

