<h3 style="margin-bottom: 11px; text-align: justify;"><span style="font-size:22px;"><strong>5.2 C&aacute;ch setup một chiến dịch quảng c&aacute;o Remarketing Facebook</strong></span></h3>

<p style="margin-bottom: 11px; text-align: justify;">Hơi kh&aacute;c những định dạng quảng c&aacute;o th&ocirc;ng thường, để triển khai Remarketing Facebook, bạn cần thực hiện c&aacute;c bước sau:</p>

<p style="margin-bottom: 11px; text-align: justify;"><strong>+ Tạo một website</strong></p>

<p style="margin-bottom: 11px; text-align: justify;">Website kh&ocirc;ng phải điều kiện bắt buộc, nhưng đến 90% nh&agrave; quảng c&aacute;o chạy Remarketing cần trang Web để tối ưu hiệu quả.</p>

<p style="margin-bottom: 11px; text-align: justify;"><strong>+ Lấy m&atilde; Pixel </strong></p>

<p style="margin-bottom: 11px; text-align: justify;">M&atilde; <a href="pixel-facebook-trong-quang-cao-facebook-la-gi/index.html">Pixel Facebook </a>l&agrave; một đoạn code đơn giản cho ph&eacute;p Facebook theo d&otilde;i, ghi nhận lại th&ocirc;ng tin người d&ugrave;ng truy cập Web.</p>

<p style="margin-bottom: 11px; text-align: justify;">Dựa v&agrave;o th&ocirc;ng tin thu thập được, Facebook sẽ truy ngược lại t&agrave;i khoản của họ để hiển thị quảng c&aacute;o.</p>

<p style="margin-bottom: 11px; text-align: justify;"><strong>+&nbsp; Ch&egrave;n m&atilde; Pixel v&agrave;o Website</strong></p>

<p style="margin-bottom: 11px; text-align: justify;">Sau khi lấy được đoạn m&atilde; Pixel, bạn cần ch&egrave;n v&agrave;o Website để Pixel hoạt động.</p>

<p style="margin-bottom: 11px; text-align: justify;"><strong>+ Tạo đối tượng t&ugrave;y chỉnh</strong></p>

<p style="margin-bottom: 11px; text-align: justify;">Tạo <a href="target-doi-tuong-facebook-hieu-qua/index.html#Target_nang_cao_voi_custom_audience" target="_blank">đối tượng t&ugrave;y chỉnh Facbook</a>, hiểu một c&aacute;ch đơn giản l&agrave; c&aacute;ch để lấy dữ liệu từ pixel v&agrave; lưu th&agrave;nh tệp ri&ecirc;ng.</p>

<p style="margin-bottom: 11px; text-align: justify;"><strong>+ Tạo nh&oacute;m quảng c&aacute;o Remarketing</strong></p>

