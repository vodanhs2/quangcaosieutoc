<p style="text-align: justify;">Điều kiện k&iacute;ch hoạt gọi ngắn gọn l&agrave; Trigger.</p>

<p style="text-align: justify;">Trigger c&oacute; thể l&agrave; thời điểm người d&ugrave;ng rời khỏi web của bạn.</p>

<p style="text-align: justify;">V&iacute; dụ: Bạn c&oacute; thể set quảng c&aacute;o hiển thị sau 3 ng&agrave;y, một quảng c&aacute;o kh&aacute;c hiển thị sau 7 ng&agrave;y, mẫu kh&aacute;c sau 14 ng&agrave;y&hellip;</p>

<p style="text-align: justify;">Trigger cũng c&oacute; thể l&agrave; h&agrave;nh vi người d&ugrave;ng tr&ecirc;n web.</p>

<p style="text-align: justify;">V&iacute; dụ: Hiện QC&nbsp;khi người d&ugrave;ng truy cập trang chủ, một quảng c&aacute;o kh&aacute;c khi họ click xem danh mục sản phẩm, một mẫu kh&aacute;c d&agrave;nh cho user đ&atilde; đi đến bước thanh to&aacute;n nhưng vẫn chưa ho&agrave;n th&agrave;nh&hellip;</p>

<p style="text-align: justify;">&nbsp; <span style="font-size:18px"><strong>&nbsp;</strong></span></p>

