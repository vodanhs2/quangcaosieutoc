</div>

<div id="Remarketing_Facebook_la_gi">
<p style="margin-bottom: 11px; text-align: justify;">Remarketing, hiểu một c&aacute;ch đơn giản l&agrave; tiếp thị lại, hiển thị lại quảng c&aacute;o cho những người đ&atilde; (hoặc chưa mua sản phẩm).</p>

<p style="margin-bottom: 11px; text-align: justify;">Điểm chung ở đ&acirc;y l&agrave; tất cả họ đều biết đến thương hiệu/ SP của bạn.</p>

<p style="margin-bottom: 11px; text-align: justify;"><strong>- Bạn c&oacute; thể hiển thị quảng c&aacute;o đến những đối tượng:</strong></p>

<p style="margin-bottom: 11px; text-align: justify;">+&nbsp; Đ&atilde; từng tương t&aacute;c với Fanpage của bạn.</p>

<p style="margin-bottom: 11px; text-align: justify;">+ Đ&atilde; xem video của bạn.</p>

<p style="margin-bottom: 11px; text-align: justify;">+ Đ&atilde; từng mua sản phẩm.</p>

<p style="margin-bottom: 11px; text-align: justify;">+ Đ&atilde; từng tương t&aacute;c với Lead Ad.</p>

<p style="margin-bottom: 11px; text-align: justify;">+ Đ&atilde; từng tham khảo website của bạn.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>

<p style="margin-bottom: 11px; text-align: justify;">+ Đ&atilde; xem web nhưng chưa quay lại trong khoảng thời gian cụ thể.</p>

<p style="margin-bottom: 11px; text-align: justify;">+ Đ&atilde; truy cập page A tr&ecirc;n website nhưng kh&ocirc;ng truy cập page B.</p>

<p style="margin-bottom: 11px; text-align: justify;">&nbsp;</p>

<p style="margin-bottom: 11px; text-align: justify;"><strong>- Nguy&ecirc;n tắc hoạt động rất đơn giản:</strong></p>

<p style="margin-bottom: 11px; text-align: justify;">+ Người d&ugrave;ng truy cập website của bạn, t&igrave;m đến trang sản phẩm A tham khảo th&ocirc;ng tin, sau đ&oacute; rời web m&agrave; chưa mua sản phẩm.</p>

<p style="margin-bottom: 11px; text-align: justify;">+ H&ocirc;m sau họ sẽ thấy đ&uacute;ng mẫu quảng c&aacute;o sản phẩm A khi lướt Facebook.</p>

<p style="margin-bottom: 11px; text-align: justify;">+ Nếu họ nhấp v&agrave;o quảng c&aacute;o sẽ được chuyển hướng trở về trang sản phẩm tr&ecirc;n Website.</p>

