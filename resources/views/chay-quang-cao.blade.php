<p style="text-align: justify;"><strong>Những đối tượng n&agrave;y đ&atilde; biết đến thương hiệu/ sản phẩm, những g&igrave; họ cần b&acirc;y giờ l&agrave; một &ldquo;động lực&rdquo; đủ mạnh để mua.</strong></p>

<p style="text-align: justify;"><strong>Những động lực n&agrave;y c&oacute; thể l&agrave;:</strong></p>

