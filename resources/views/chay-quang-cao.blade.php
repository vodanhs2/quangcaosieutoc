<p style="text-align: justify;"><span style="font-size:20px;"><strong>-&nbsp;&Yacute; tưởng sơ bộ</strong></span></p>

<p style="text-align: justify;">Giả sử bạn đang kinh doanh c&agrave; ph&ecirc; cao cấp chẳng hạn.</p>

<p style="text-align: justify;">Khi triển khai quảng c&aacute;o, phần lớn c&ocirc;ng ty sẽ tư vấn thực hiện theo quy tr&igrave;nh remarketing cơ bản.&nbsp;</p>

<p style="text-align: justify;"><span style="font-size:20px;"><strong>+ Quy tr&igrave;nh ti&ecirc;u chuẩn truyền thống</strong></span></p>

<p style="text-align: justify;"><strong>Ng&agrave;y 1:</strong> Người d&ugrave;ng truy cập Web.</p>

<p style="text-align: justify;"><strong>Ng&agrave;y 2:</strong> Người d&ugrave;ng sẽ thấy&nbsp;quảng c&aacute;o sản phẩm c&agrave; ph&ecirc; của bạn.</p>

<p style="text-align: justify;"><span style="font-size:20px;"><strong>+ Quy tr&igrave;nh chiến dịch li&ecirc;n ho&agrave;n&nbsp;</strong></span></p>

<p style="text-align: justify;"><strong>Ng&agrave;y 1:</strong> Người d&ugrave;ng truy cập Web.</p>

<p style="text-align: justify;"><strong>Ng&agrave;y 2:</strong> User thấy quảng c&aacute;o về c&aacute;c mẹo pha c&agrave; ph&ecirc; ngon.</p>

<p style="text-align: justify;"><strong>Ng&agrave;y 3:</strong> User thấy quảng c&aacute;o về những điều cần tr&aacute;nh khi mua c&agrave; ph&ecirc;.</p>

<p style="text-align: justify;"><strong>Ng&agrave;y 4:</strong> Họ thấy quảng c&aacute;o giảm gi&aacute; 10% cho sản phẩm.</p>

<p style="text-align: justify;"><em>(Nếu User th&ecirc;m sản phẩm v&agrave;o giỏ h&agrave;ng nhưng chưa thanh to&aacute;n)</em></p>

<p style="text-align: justify;"><strong>- Ng&agrave;y 5:</strong> User thấy quảng c&aacute;o sale off 10% + sản phẩm bonus.</p>

<p style="text-align: justify;"><em>(Nếu user thực hiện thanh to&aacute;n sau khi nh&igrave;n thấy quảng c&aacute;o)</em></p>

