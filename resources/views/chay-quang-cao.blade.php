<p style="text-align: justify;"><span style="font-size:20px"><strong>+ Truy cập Custom Audience</strong></span></p>

<p style="text-align: justify;">&nbsp;<span style="font-size:20px;"><strong>Đối với Trigger thời gian</strong></span></p>

