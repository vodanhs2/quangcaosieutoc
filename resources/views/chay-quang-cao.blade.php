<div id="Quang_cao_facebook_la_gi">
<p style="margin-bottom: 11px; text-align: justify;">Quảng c&aacute;o Facebook, hay Facebook Ads l&agrave; một dịch vụ trả ph&iacute; của Facebook. Hiểu đơn giản nhất, bạn sẽ chi tiền để được hiển thị quảng c&aacute;o ở những vị tr&iacute; được quy định sẵn của Facebook.</p>

<p style="margin-bottom: 11px; text-align: justify;">Khi người d&ugrave;ng lướt Facebook, họ sẽ nh&igrave;n thấy quảng c&aacute;o tr&ecirc;n NewFeed (hoặc cột phải) k&egrave;m d&ograve;ng chữ &ldquo;Được t&agrave;i trợ&rdquo;.</p>

<blockquote>
<p style="margin-bottom: 11px; text-align: justify;"><strong>Nếu định nghĩa tr&ecirc;n chưa đủ chi tiết đối với bạn, h&atilde;y t&igrave;m hiểu b&agrave;i viết <a href="chay-quang-cao-facebook-ads-la-gi/index.html" target="_blank">quảng c&aacute;o Facebook l&agrave; g&igrave;</a>.</strong></p>
</blockquote>
