<p style="text-align: justify;"><strong>A/B testing hay c&ograve;n gọi l&agrave; Split Testing, </strong>l&agrave; một t&iacute;nh năng được t&iacute;ch hợp sẵn trong t&igrave;nh quản l&iacute; quảng c&aacute;o<strong>.</strong></p>

<p style="text-align: justify;">Ở ngay bước đầu khi chọn mục ti&ecirc;u&nbsp;quảng c&aacute;o, Facebook cho ph&eacute;p bạn k&iacute;ch hoạt t&iacute;nh năng <strong>Tạo Thử Nghiệm Ph&acirc;n T&aacute;ch</strong>.</p>

