<p style="margin-bottom: 11px; text-align: justify;">Trong qu&aacute; tr&igrave;nh quảng c&aacute;o Facebook được ph&acirc;n phối, bạn vẫn c&oacute; thể chỉnh sửa lại nh&oacute;m đối tượng, ng&acirc;n s&aacute;ch, bật tắt quảng c&aacute;o&hellip;Bất kỳ l&uacute;c n&agrave;o.</p>

<p style="margin-bottom: 11px; text-align: justify;">Dưới đ&acirc;y l&agrave; một số thao t&aacute;c cơ bản m&agrave; Newbie cần biết.</p>

<p style="margin-bottom: 11px; text-align: justify;">&nbsp;</p>

<h4 style="margin-bottom: 11px; text-align: justify;"><span style="font-size:22px;"><strong>- Chỉnh sửa chiến dịch v&agrave; nh&oacute;m quảng c&aacute;o Facebook</strong></span></h4>

<p style="margin-bottom: 11px; text-align: justify;">Ch&uacute;ng ta thường xuy&ecirc;n chỉnh sửa nh&oacute;m quảng c&aacute;o, &iacute;t khi chỉnh chiến dịch.</p>

<p style="margin-bottom: 11px; text-align: justify;">Thao t&aacute;c thực hiện như sau:</p>

<p style="margin-bottom: 11px; text-align: justify;">+ Truy cập <strong>Tr&igrave;nh Quản L&yacute; QC.</strong></p>

<p style="margin-bottom: 11px; text-align: justify;">+ Stick v&agrave;o t&ugrave;y chọn<strong> Nh&oacute;m Quảng C&aacute;o</strong>.</p>

<p style="margin-bottom: 11px; text-align: justify;">+ Stick v&agrave;o<strong> Nh&oacute;m Quảng C&aacute;o</strong> cần chỉnh sửa. N&uacute;t <strong>Chỉnh Sửa </strong>sẽ chuyển trạng th&aacute;i khả dụng.</p>
