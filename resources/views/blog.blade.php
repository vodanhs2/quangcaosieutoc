            $go_to=$('.row:last').offset().top;
            $.get(			
                $url+'show-blog-pagination/',
                {start:$start},
                function(data){
                    if(data){
                        $('#block').append(data);
                        $more.removeClass('clicked').html($text_default);
                        $('html, body').animate({scrollTop:$go_to},800);
                        $start+=5;// 10 giá trị load ra
                    }else{
                        $more.remove();
                    }
                }
            );
        }

    });
